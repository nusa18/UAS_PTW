     */
    public function index(Request $request)
    {
        $user = DB::table('users')
        ->orderBy('name', 'desc')
        ->where('role', '=', 'member')
        ->paginate(10);
        $data = array('title' => 'Data Customer',
            'user'=> $user,
        );
        return view('customer.index', $data)->with('no', ($request->input('page', 1) - 1) * 20);
    }

